package Modelo;

import javax.swing.ImageIcon;

public class ModeloUsuario {

    private int codigo;
    private String usario;
    private String correo;
    private String contrase;
    private String tido_usuario;
    private String foto;
    private ImageIcon fotos;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getUsario() {
        return usario;
    }

    public void setUsario(String usario) {
        this.usario = usario;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getContrase() {
        return contrase;
    }

    public void setContrase(String contrase) {
        this.contrase = contrase;
    }

    public String getTido_usuario() {
        return tido_usuario;
    }

    public void setTido_usuario(String tido_usuario) {
        this.tido_usuario = tido_usuario;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    public ImageIcon getFotos() {
        return fotos;
    }

    public void setFotos(ImageIcon fotos) {
        this.fotos = fotos;
    }
    
    

  
   

}
